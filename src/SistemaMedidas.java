public class SistemaMedidas {
    public static void main(String[] args) {
        String sigla = "P";

        if(sigla == "P")
         System.out.println("Pequeno");

        else if (sigla == "M")
         System.out.println("Médio");

        else if (sigla == "G")
         System.out.println("Médio");

        else
        System.out.println("TAMANHO INDEFINIDO"); 
    }
}
